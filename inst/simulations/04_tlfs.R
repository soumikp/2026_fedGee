#| label: tbl-sim-coverage
#| tbl-cap: "Performance of Variance Estimators by Number of Network Sites ($K$). Coverage probabilities and Standard Error to Empirical Standard Deviation (SE/SD) ratios are shown for varying sandwich architectures and small-sample corrections within the FedGEE framework. Target coverage is 0.950; target SE/SD ratio is 1.00. *Note*: Values reflect simulations where $\\rho_H > 0$. Patient-level sandwich assumes independence between patients. The SE/SD ratio is identical for the site-level $z$ and $t$ rows (same standard error estimate; only the critical value differs) and is reported once. MD = Mancl-DeRouen correction."
#| echo: false
#| warning: false
#| message: false

# ── Patient-level sandwich (z-based) ────────────────────────────────────────
tbl_patient <- all_res |>
  filter(rho_H > 0, method == "fed_patient",
         !is.na(beta_hat), !is.na(se_hat), !is.na(covers_z)) |>
  group_by(K) |>
  summarise(
    cov_patient  = mean(covers_z),
    sesd_patient = mean(se_hat) / sd(beta_hat),
    .groups = "drop"
  )

# ── Site-level sandwich, z-based (fed_site) ──────────────────────────────────
tbl_site_z <- all_res |>
  filter(rho_H > 0, method == "fed_site",
         !is.na(beta_hat), !is.na(se_hat), !is.na(covers_z)) |>
  group_by(K) |>
  summarise(
    cov_site_z  = mean(covers_z),
    sesd_site   = mean(se_hat) / sd(beta_hat),
    .groups = "drop"
  )

# ── Site-level sandwich, t-based (fed_site) ──────────────────────────────────
tbl_site_t <- all_res |>
  filter(rho_H > 0, method == "fed_site",
         !is.na(beta_hat), !is.na(se_hat), !is.na(covers_t)) |>
  group_by(K) |>
  summarise(
    cov_site_t = mean(covers_t),
    .groups = "drop"
  )

# ── Site-level + MD correction, t-based (fed_site_md) ────────────────────────
tbl_md <- all_res |>
  filter(rho_H > 0, method == "fed_site_md",
         !is.na(beta_hat), !is.na(se_hat), !is.na(covers_t)) |>
  group_by(K) |>
  summarise(
    cov_md  = mean(covers_t),
    sesd_md = mean(se_hat) / sd(beta_hat),
    .groups = "drop"
  )

# ── Combine ──────────────────────────────────────────────────────────────────
tbl_final <- tbl_patient |>
  left_join(tbl_site_z, by = "K") |>
  left_join(tbl_site_t, by = "K") |>
  left_join(tbl_md,     by = "K") |>
  arrange(K)

# ── Format for display ──────────────────────────────────────────────────────
tbl_display <- tbl_final |>
  transmute(
    `$K$`                                        = K,
    `Patient Sandwich<br>Coverage`               = sprintf("%.3f", cov_patient),
    `Patient Sandwich<br>SE/SD`                  = sprintf("%.2f", sesd_patient),
    `Site Sandwich ($z$)<br>Coverage`             = sprintf("%.3f", cov_site_z),
    `Site Sandwich<br>SE/SD`                      = sprintf("%.2f", sesd_site),
    `Site Sandwich ($t$)<br>Coverage`             = sprintf("%.3f", cov_site_t),
    `Site + MD ($t$)<br>Coverage`                 = sprintf("%.3f", cov_md),
    `Site + MD ($t$)<br>SE/SD`                    = sprintf("%.2f", sesd_md)
  )

kable(tbl_display, align = c("l", rep("c", 7)), escape = FALSE)

