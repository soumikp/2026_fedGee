#| label: fig-sim-bias
#| fig-cap: "**Point Estimation and Bias in Federated vs. Traditional Networks.** All panels use $K = 25$ and $\\beta_0 = -1$ (common outcome) unless otherwise noted. **(A)** Mean absolute bias heatmap of FedGEE vs. Meta-Analytic GLM across the full $\\rho_H \\times \\rho_P$ grid, illustrating the degradation of traditional meta-analysis as between-hospital correlation grows. **(B)** Mean absolute bias under a rare outcome ($\\beta_0 = -3$), demonstrating the robustness of the federated Newton-Raphson update when local site data is sparse. **(C)** Histogram of the site-level covariate estimates ($\\hat{\\beta}_{\\text{teach}}$) generated by FedGEE, showing the algorithm's ability to accurately recover site-constant effects strictly from between-site variation. **(D)** Scatterplot establishing mathematical parity: FedGEE coefficient estimates plotted directly against Centralized Pooled GEE estimates. The maximum absolute difference across all replications in this scenario is annotated."
